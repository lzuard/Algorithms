/** Список всех возможных команд **/
public enum CMD {
    CHESS,      // Шахматы
    HELP,       // Вывести подсказку
    STOP,       // Остановить программу
    GENERATE,   // Сгенерировать набор размера n
    ADD,        // Добавить значение
    DELETE,     // Удалить значение
    FIND,       // Найти значение
    PRINT,      // Вывести на экран список всех значений и их хешей
}
